    private void qtyKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_qtyKeyReleased
             
        try
        {
            int q=Integer.parseInt(qty.getText());
            int x=prod1.getSelectedIndex();
            int y=(int) v1.get(x-1);
            Connection con1=DBConnection.getConn();
            Statement stm=con1.createStatement();
            ResultSet rs= stm.executeQuery("select qty from stock where product_id="+y);
            int old=0;
            if(rs.next())
            {
                old=rs.getInt(1);
            }
            int nw=old-q;
            if(nw<0)
            {
                 JOptionPane.showMessageDialog(null, "only "+old+" in stock");
            }
            
        }catch(Exception ee)
        {
            System.out.println(ee);
        }
    }//GEN-LAST:event_qtyKeyReleased

