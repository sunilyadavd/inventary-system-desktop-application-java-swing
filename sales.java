    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        if(validateField())
        {
        salesDTO dto =new salesDTO();
        dto.setInv_no(invno);//set inv no
        // product id
        int x=prod1.getSelectedIndex();
        int y=(int) v1.get(x-1);
        dto.setProduct_id(y);
        //customer id
        int x1=cust.getSelectedIndex();
        int y1=(int)c1.get(x1-1);
        dto.setCustomer_id(y1);
        //Quantity
        int q=Integer.parseInt(qty.getText());
        dto.setQuantity(q);
        //Totol price
        int pp=Integer.parseInt(prprice1.getText());
        pp=pp*q;
        dto.setTotal_amount(pp);
        //set date
        DateFormat df = new SimpleDateFormat("dd/MM/yyyy");
        String date = df.format(new Date());
        dto.setSale_date(date);
        salesDAO dao=new salesDAO();
        int r=dao.insert(dto);
        
       
         try
         {
             if(r==1)
            {
                Connection con1=DBConnection.getConn();
                Statement stm=con1.createStatement();
                ResultSet rs= stm.executeQuery("select qty from stock where product_id="+y);
                int old=0;
                if(rs.next())
                {
                    old=rs.getInt(1);
                }
                int nw=old-q;  
                PreparedStatement ps=con1.prepareStatement("update stock set qty=? where product_id="+y);
                ps.setInt(1,nw);
                int n=ps.executeUpdate();
                if(n==1)
                {
                 System.out.println("quantity updated in stock also");
                }
            }else
            {
               System.out.println("quantity  not updated in stock");
            }
        
         }catch(Exception ee)
         {
             System.out.println(ee);
         }
        }
    }//GEN-LAST:event_saveActionPerformed

