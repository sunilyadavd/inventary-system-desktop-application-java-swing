    private void prod1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_prod1ActionPerformed
        int x=prod1.getSelectedIndex();
        int y=(int) v1.get(x-1);
        reportDAO dao=new reportDAO();
        int stock=dao.getStock(y);
        //inserting data inti 1st table
        model.insertRow(tb1Data.getRowCount(),new Object[]{
            y,stock
        });
     //getting data from purchase table
        ResultSet rs=dao.getpurchase(y);
       
        try
        {
        int tup1=0;    
        while(rs.next())
        {
           tup1=tup1+rs.getInt(1);//for total unit purchased
           model1.insertRow(tb2data.getRowCount(),new Object[]{
            rs.getInt(1),
            rs.getString(2)
        });
        }
        //setting total unit purchase
        tup.setText(tup1+"");
        
        }catch(Exception ee)
        {
            System.out.println(ee);
        }
        

        
        //getting data from sale table
        ResultSet rs1=dao.getsale(y);
       
        try{
        int tus1=0;
        while(rs1.next())
        {
            tus1=tus1+rs1.getInt(1);
           model2.insertRow(tb3data.getRowCount(),new Object[]{
            rs1.getInt(1),
            rs1.getString(2)
        });
        }
        tus.setText(tus1+"");
        }catch(Exception ee)
        {
            System.out.println(ee);
        }
    }//GEN-LAST:event_prod1ActionPerformed

