    private void selecttypeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selecttypeActionPerformed
        int x=selecttype.getSelectedIndex();
        if(x==0)
        {
            
            Vector c1,c2;
            purchaseDAO dao=new purchaseDAO();
            List t2=dao.getCustomer();
            c1=(Vector) t2.get(0);
            c2=(Vector) t2.get(1);
        for(int i=0;i<c2.size();i++)
        {
            cusname.addItem((String)c2.get(i));
        }
  
        for(int i=0;i<v2.size();i++)
        {
            prod1.addItem((String)v2.get(i));
        }
            
        }
        else
        {
             Vector c1,c2;
            salesDAO dao=new salesDAO();
            List t2=dao.getCustomer();
            c1=(Vector) t2.get(0);
            c2=(Vector) t2.get(1);
            for(int i=0;i<c2.size();i++)
            {
                cusname.addItem((String)c2.get(i));
            }
        }
    }//GEN-LAST:event_selecttypeActionPerformed

